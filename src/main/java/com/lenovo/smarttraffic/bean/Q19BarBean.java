package com.lenovo.smarttraffic.bean;

public class Q19BarBean {
    String title;
    int num;

    public Q19BarBean(String title, int num) {
        this.title = title;
        this.num = num;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
